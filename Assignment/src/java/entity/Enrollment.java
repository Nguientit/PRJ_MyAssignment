package entity;

/**
 *
 * @author Admin
 */
public class Enrollment {
    private int sid;
    private int gid;

    public Enrollment() {
    }

    public Enrollment(int sid, int gid) {
        this.sid = sid;
        this.gid = gid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getGid() {
        return gid;
    }

    public void setGid(int gid) {
        this.gid = gid;
    }
    
}
